/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public ListNode getKthFromEnd(ListNode head, int k) {
        ListNode p = head, q = head;
        while (k > 0 && q != null) {
            q = q.next;
            k--;
        }
        while (q != null) {
            p = p.next;
            q = q.next;
        }
        return p;
    }
}
